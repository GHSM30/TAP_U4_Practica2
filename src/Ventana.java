    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int renglonSeleccionado = jTable1.getSelectedRow();
        if(renglonSeleccionado == -1){
            JOptionPane.showMessageDialog(this, "ERROR DEBES DE SELECCIONAR UN RENGLON PARA BORRAR");
            return;
        }
        String idBorrar = tabla.getValueAt(renglonSeleccionado, 0).toString();
        boolean resultadoEliminar = eliminar(idBorrar);
        int res = JOptionPane.showConfirmDialog(this, "ESTAS SEGURO DE ELIMINAR ESE RENGLON");
        if(res == JOptionPane.YES_OPTION){
                if(resultadoEliminar == true){
                mostrarDatos();
                JOptionPane.showMessageDialog(this, "EXITO SE BORRO EL ARTICULO SELECCIONADO");
            }else{
                JOptionPane.showMessageDialog(this, "ERROR NO SE PUDO BORRAR EL ARTICULO");
            }
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

