     public void mostrarDatos(){
        MongoCursor<Document> consulta = almacen.find().iterator();
        //se elimnan los datos de una posible consulta anterior
        int total = tabla.getRowCount();
        for(int i = 0; i<total; i++){
            tabla.removeRow(0);
        }
        
        //se vuelve a llenar la tabla con los datos obtendos con la consulta find
        while(consulta.hasNext()){
            ArrayList<Object> doc = new ArrayList<Object>(consulta.next().values());
            tabla.addRow(doc.toArray());
        }
        
    }
     
     public boolean eliminar(String id){
        DeleteResult resultado = almacen.deleteOne(new Document("_id",new ObjectId(id)));
        if(resultado.getDeletedCount() == 1){
            return true;
        }
        return false;    
     }
     
     public void actualizar(){
        Document datosActualizados = new Document();
         
        datosActualizados.put("nombre",jTextField7.getText());
        datosActualizados.put("peso",Integer.parseInt(jTextField8.getText()));
        datosActualizados.put("cantidad",Integer.parseInt(jTextField9.getText()));
        datosActualizados.put("precio",Double.parseDouble(jTextField10.getText()));
        datosActualizados.put("disponibilidad",jTextField11.getText());
        datosActualizados.put("direccion",jTextField12.getText());
        
        UpdateResult resultado = almacen.updateOne(filtro, new Document("$set", datosActualizados));
        
        if(resultado.getMatchedCount()==1){
            JOptionPane.showMessageDialog(this, "EXITO SE ACTUALIZO EL ARTICULO");
        }else{
            JOptionPane.showMessageDialog(this, "ERROR NO SE PUDO REALIZAR LA ACTUALIZACION");
        }
        
     }
